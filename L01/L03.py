# Програма зчитує двоцифрове число і виводить через пропуск кожну
# цифру окремо.
# Вхідні дані
# Натуральне число на проміжку від 10 до 99 включно.
# Вихідні дані
# Спочатку першу цифру числа і через пропуск другу.
# Вхідні дані #1
# 23
# Вихідні дані #1
# 2 3

n = int(input())

des = n // 10
od = n % 10

print(des, od)