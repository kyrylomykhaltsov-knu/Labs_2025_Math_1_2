import random

# користувач вводить 12 слів через пробіл або кому
line = input("Введіть прізвища 12 студентів через пробіл або кому: ")

# розділяємо рядок на список слів
# спочатку замінюємо коми на пробіли, потім split()
words = line.replace(",", " ").split()

# перевіряємо, що слів рівно 12
if len(words) != 12:
    print("Помилка: потрібно ввести рівно 12 слів!")
else:
    # перемішуємо список
    random.shuffle(words)

    # ділимо на групи по 3
    groups = [words[i:i+3] for i in range(0, 12, 3)]

    # виводимо результат
    for idx, group in enumerate(groups, start=1):
        print(f"Група {idx}: {group}")


