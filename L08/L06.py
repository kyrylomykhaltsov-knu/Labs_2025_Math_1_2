# Завдання:
# F(1) = F(2) = 1
# F(n) = F(n-1) + F(n-2) для n >= 3
# Потрібно обробити t тестів і для кожного n вивести F(n).

# 1) Зчитуємо кількість тестів t.
t = int(input().strip())

# 2) Зчитуємо всі n у список. Так зручніше підготувати відповіді.
ns = []
for _ in range(t):
    ns.append(int(input().strip()))

# 3) Щоб не рахувати Фібоначчі кожного разу з нуля,
#    один раз побудуємо таблицю значень до найбільшого n.
max_n = 0
for x in ns:
    if x > max_n:
        max_n = x

# 4) Порожній випадок (раптом t=0) — нічого не робимо.
#    Далі будуємо масив F так, що F[i] = i-те число Фібоначчі.
#    Створюємо список довжиною max_n + 1 (щоб індекси 1..max_n існували).
F = [0] * (max_n + 1)

# Базові випадки з умови:
if max_n >= 1:
    F[1] = 1
if max_n >= 2:
    F[2] = 1

# 5) Ітеративно (циклом) будуємо послідовність:
#    Кожен наступний елемент — сума двох попередніх.
i = 3
while i <= max_n:
    F[i] = F[i - 1] + F[i - 2]
    i += 1

# 6) Виводимо відповіді для кожного запиту.
for n in ns:
    print(F[n])
