# Зчитуємо коефіцієнти у двовимірний список (багатовимірний масив)
matrix = []                 # тут буде 2 рядки по 3 числа
for i in range(2):          # два рівняння
    row = [float(x) for x in input().split()]
    matrix.append(row)      # додаємо рядок до матриці

# Розпаковуємо коефіцієнти з матриці
a11, a12, b1 = matrix[0]    # перший рядок
a21, a22, b2 = matrix[1]    # другий рядок

# Обчислюємо визначники за формулами Крамера
Delta  = a11 * a22 - a12 * a21
Delta1 = b1  * a22 - a12 * b2
Delta2 = a11 * b2  - b1  * a21

# Знаходимо корені
x1 = Delta1 / Delta
x2 = Delta2 / Delta

# Виводимо з точністю 0.001 (три знаки після коми)
print(f"{x1:.3f}")
print(f"{x2:.3f}")
