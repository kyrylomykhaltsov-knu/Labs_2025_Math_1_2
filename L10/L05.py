# Зчитуємо матрицю 3x3 у вигляді двовимірного масиву

a = []                     # тут буде список з 3 рядків

for i in range(3):         # 3 рядки
    row = list(map(int, input().split()))  # зчитуємо рядок з 3 чисел
    a.append(row)          # додаємо рядок до матриці

# Обчислюємо детермінант за формулою для 3x3

det = (a[0][0] * a[1][1] * a[2][2] +
       a[0][1] * a[1][2] * a[2][0] +
       a[0][2] * a[1][0] * a[2][1] -
       a[0][2] * a[1][1] * a[2][0] -
       a[0][0] * a[1][2] * a[2][1] -
       a[0][1] * a[1][0] * a[2][2])

# Виводимо магічне число Ватсона (детермінант)

print(det)
