# 1. Зчитуємо розмір матриці
n = int(input())

# 2. Створюємо порожній список для матриці
M = []

# 3. Зчитуємо n рядків матриці
for i in range(n):
    # зчитуємо рядок, ділимо по пробілах, перетворюємо на int
    row = [int(x) for x in input().split()]
    M.append(row)   # додаємо рядок до матриці

# 4. Зчитуємо r і c
r, c = map(int, input().split())

# 5. Виводимо підматрицю перших r рядків і перших c стовпців
for i in range(r):          # проходимо по перших r рядках
    for j in range(c):      # у кожному рядку беремо перші c елементів
        print(M[i][j], end=" ")
    print()                 # перехід на новий рядок після кожного рядка
