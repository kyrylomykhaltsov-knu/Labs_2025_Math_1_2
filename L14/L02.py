# Задано рядок, серед елементів якого містяться цифри.
# Використовуючи для всіх символів рядка функцію int перетворення
# символа у число, обчислити суму цифр заданого рядка.

# "1238923"

s = input("Задайте рядок з цифрами: ")

suma = 0
for c in s:
    try:
        d = int(c)
        suma += d
    except ValueError:
        pass
print(f"сума цифр = {suma}")