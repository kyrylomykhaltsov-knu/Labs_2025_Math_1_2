# Програма для обчислення середнього чека в кафе
#
# Написати програму, яка обчислює середній чек у кафе.
# Користувач вводить:
#     - загальну суму виручки за день (у гривнях),
#     - кількість відвідувачів.
# Програма повинна:
#     1. Обчислити середній чек:
#     середній чек = виручка / кількість відвідувачів
#
# 2. Коректно опрацювати виключні ситуації:
#     - якщо користувач вводить не число (наприклад, "сто" замість 100) — вивести повідомлення про помилку введення;
#     - якщо кількість відвідувачів дорівнює 0 — вивести повідомлення, що ділити на нуль не можна;
#     - якщо виручка або кількість відвідувачів — від’ємні числа, викинути ValueError самостійно (за допомогою raise) і теж вивести повідомлення про помилку.
# 3. Використати блоки try, except, else, finally.

try:
    # 1. Зчитуємо дані від користувача як рядки
    total = float(input("Введіть загальну суму виручки за день (грн): ")) # виручка може бути з копійками
    visitors = int(input("Введіть кількість відвідувачів за день: ")) # кількість відвідувачів - ціле число

    # 2. Перевіряємо, чи дані мають сенс
    if total < 0 or visitors < 0:
        # Самостійно створюємо виключення, якщо значення некоректні
        raise ValueError("Виручка і кількість відвідувачів не можуть бути від’ємними.")

    # 3. Обчислюємо середній чек
    average_check = total / visitors  # тут може виникнути ZeroDivisionError, якщо visitors == 0

except ValueError as error:
    # Спрацьовує, якщо:
    # - не вдалося перетворити введені дані на числа
    # - або ми самі зробили raise ValueError при від’ємних значеннях
    print("Помилка введення даних:", error)

except ZeroDivisionError:
    # Спрацьовує, якщо visitors == 0 і відбулось ділення на нуль
    print("Неможливо обчислити середній чек: кількість відвідувачів дорівнює нулю.")

else:
    # Виконується лише якщо в блоці try НЕ було жодного виключення
    print("Середній чек за день становить:", average_check, "грн")

finally:
    # Виконається завжди, незалежно від того, була помилка чи ні
    print("Роботу програми завершено.")
